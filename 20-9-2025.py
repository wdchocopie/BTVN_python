# python là gì? 
# Python là một ngôn ngữ lập trình đa mục đích, dễ học và sử dụng.
# Python được sử dụng rộng rãi trong phát triển web, khoa học dữ liệu, trí tuệ nhân tạo, tự động hóa và nhiều lĩnh vực khác.
# Python có cú pháp đơn giản, dễ đọc và viết, giúp lập trình viên tập trung
# vào giải quyết vấn đề thay vì lo lắng về cú pháp phức tạp.
# Python có một cộng đồng lớn và nhiều thư viện hỗ trợ, giúp việc phát triển ứng dụng nhanh chóng và hiệu quả.

# Hàm print:
# sử dụng để in ra màn hình
print("Hello, World!")
# đặt biệt, có thể in nhiều giá trị cùng lúc
print("Hello", "World", "from", "Python")
# gọi biến
x = 42
y = "The answer is"
# in ra biến
print(y, x)
# Hàm input:
# sử dụng để nhận dữ liệu từ bàn phím
name = input("Enter your name: ")
print("Hello, " + name + "!")
# hàm type trả về kiểu dữ liệu của biến
print("Type of x:", type(x))
print("Type of y:", type(y))


# chương trình mẫu tương tác với người dùng
age = input("Enter your age: ")
age = int(age)  # chuyển đổi chuỗi nhập vào thành số nguyên
next_year_age = age + 1
print("Next year, you will be", next_year_age, "years old.")


